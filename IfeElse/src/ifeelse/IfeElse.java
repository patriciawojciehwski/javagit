package ifeelse;

public class IfeElse {
    
    public static void main(String[] args) {
    boolean var1 = true;
    //Se var1 for verdadeiro
    if (var1){
        System.out.println("var1: Verdadeiro");
    }else{
        //opção falsa
        System.out.println("var1: Falso");
    }
    //Se var1 for falso
   if(!var1){
       System.out.println("var1: Verdadeiro");
    }else{
       System.out.println("var1: Falso");
   }
    }
    
}
